#import "UIColor+NeueColors.h"

@implementation UIColor (NeueColors)

+ (UIColor *)neueRed
{
	return [UIColor colorWithRed:1.0f green:0.23f blue:0.19f alpha:1.0f];
}

+ (UIColor *)neueGreen
{
	return [UIColor colorWithRed:0.3f green:0.85f blue:0.39f alpha:1.0f];
}

+ (UIColor *)neueBlue
{
	return [UIColor colorWithRed:0.0f green:0.48f blue:1.0f alpha:1.0f];
}

@end
